  function t(key) {
    if (window.CODEX_DRIFT && Object.prototype.hasOwnProperty.call(T_DRIFT, key)) {
      return T_DRIFT[key];
    }
    const lang = window.CODEX_LANG || "en";
    const dict = T[lang] || T.en;
    if (Object.prototype.hasOwnProperty.call(dict, key)) return dict[key];
    if (Object.prototype.hasOwnProperty.call(T.en, key))  return T.en[key];
    return key;
  }

  // Apply language-level effects (RTL flip + lang attribute) so fonts pick
  // the right script and screen readers announce correctly.
  function applyLangSideEffects(lang) {
    document.documentElement.setAttribute("lang", lang);
    document.documentElement.setAttribute("dir", RTL.has(lang) ? "rtl" : "ltr");
  }

  window.CODEX_LANG = "en";
  window.CODEX_LANGS = LANGS;
  window.CODEX_T = T;
  window.CODEX_T_DRIFT = T_DRIFT;
  window.CODEX_DRIFT = false;
  window.t = t;
  window.applyCodexLang = (lang) => {
    if (!T[lang]) return;
    window.CODEX_LANG = lang;
    applyLangSideEffects(lang);
    // Notify subtrees that resolve t() lazily (Oracle, panels) so they
    // re-render or re-fetch in the new language.
    try { window.dispatchEvent(new CustomEvent("codex:lang", { detail: { lang } })); } catch {}
  };
  // Map an i18n id to the human-readable name we tell the LLM to write in.
  window.codexLangName = (id) => {
    const map = { en: "English", es: "Spanish", de: "German", pt: "Portuguese",
                  fr: "French", la: "Latin", he: "Hebrew", el: "Greek", hi: "Hindi" };
    return map[id || window.CODEX_LANG || "en"] || "English";
  };
