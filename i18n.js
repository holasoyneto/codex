  function t(key) {
    if (window.CODEX_DRIFT && Object.prototype.hasOwnProperty.call(T_DRIFT, key)) {
      return T_DRIFT[key];
    }
    const lang = window.CODEX_LANG || "en";
    const dict = T[lang] || T.en;
    if (Object.prototype.hasOwnProperty.call(dict, key)) return dict[key];
    if (Object.prototype.hasOwnProperty.call(T.en, key))  return T.en[key];
    return key;
  }

  // Apply language-level effects (RTL flip + lang attribute) so fonts pick
